import java.util.*;
public class Switch {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        switch (num) {
            case 1:System.out.println("Hii Uday");
                break;
            case 2:System.out.println("Hii Tarun");
                break;
            default:System.out.println("Hii Pallavi");
        }
        
    }
    
}
